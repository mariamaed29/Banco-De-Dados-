CREATE DATABASE TRANSPORTADORA;

USE TRANSPORTADORA;

CREATE TABLE CLIENTE (
COD_CLIENTE int,
CPF_CLIENTE VARCHAR (14),
ENDERECO VARCHAR (100),
CONTATO VARCHAR (18)
);

CREATE TABLE PEDIDO (
COD_PEDIDO INT,
NUMERO_PEDIDO VARCHAR (100),
TIPO_CARGA VARCHAR (100),
ORIGEM VARCHAR (100),
DESTINO VARCHAR (100)
);

CREATE TABLE CARGA (
COD_CARGA INT,
DESCRICAO VARCHAR (150),
PESO INT,
VALUME INT,
VALOR VARCHAR (250)
);

CREATE TABLE MOTORISTA (
COD_MOTORISTA INT,
NOME_MOTORISTA VARCHAR (100),
CPF VARCHAR (14),
CNH VARCHAR (12),
TELEFONE VARCHAR (20)
);

CREATE TABLE VEICULO (
COD_VEICULO INT,
PLACA VARCHAR (10),
MODELO VARCHAR (50),
TIPO VARCHAR (30),
CAPACIDADE_CARGA INT
);






